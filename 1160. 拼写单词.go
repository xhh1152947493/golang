/*
给你一份『词汇表』（字符串数组） words 和一张『字母表』（字符串） chars。

假如你可以用 chars 中的『字母』（字符）拼写出 words 中的某个『单词』（字符串），那么我们就认为你掌握了这个单词。

注意：每次拼写（指拼写词汇表中的一个单词）时，chars 中的每个字母都只能用一次。

返回词汇表 words 中你掌握的所有单词的 长度之和。

1 <= words.length <= 1000
1 <= words[i].length, chars.length <= 100
所有字符串中都仅包含小写英文字母

*/

func countCharacters(words []string, chars string) int {
	res := 0
	for _, word := range words {
		flag := true
		tmpChars := chars
		for _, char := range word { // char 为ascii码，用string转换为char
			containFlag := strings.Contains(tmpChars, string(char))
			//fmt.Println("ASCII: ", char, "CHAR: ", string(char), "containFlag: ", containFlag)
			if !containFlag {
				flag = false
				break
			} else {
				tmpChars = strings.Replace(tmpChars, string(char), "", 1) // strings.Replace返回新串，原串不会变
			}
		}
		if flag {
			res += len(word)
		}
	}
	return res
}

